def l_star(eps: float) -> float:
    """Logarithmic bounce length L*(ε) = (rs/2) ln(1/ε) (#132)."""
    return (RS / 2.0) * math.log(1.0 / eps)


def ln_mass_diff_ratio(masses: dict) -> tuple:
    """(gen-differences of ln m, their ratio) — a clean power/geometric law
    would give equal differences (ratio 1)."""
    lnm = np.log(list(masses.values()))
    diffs = np.diff(lnm)
    return [round(float(d), 2) for d in diffs], round(float(diffs[1] / diffs[0]), 2)


