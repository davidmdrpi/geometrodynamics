# T2. Mechanism: log-bounce ⟹ power-law mass
# ---------------------------------------------------------------------------

def test_T2_power_law_mechanism() -> dict:
    """S = c·L*(ε) = c(rs/2)ln(1/ε) ⟹ m = m_0 e^{−S} = m_0 ε^{c rs/2} = m_0 ε^p.
    The logarithm turns the exponential into a power law in the penetration
    depth ε. Verify the identity e^{−c L*(ε)} = ε^p."""
    rows = []
    ok = True
    for eps, c in ((0.01, 2.0), (0.1, 1.0), (0.001, 3.6)):
        p = c * RS / 2.0
        m_exp = math.exp(-c * l_star(eps))
        m_pow = eps ** p
        match = abs(m_exp - m_pow) < 1e-12
        ok = ok and match
        rows.append({'eps': eps, 'c': c, 'p_eq_c_rs_over_2': p,
                     'e_minus_cLstar': round(m_exp, 8),
                     'eps_to_p': round(m_pow, 8), 'match': match})
    return {
        'name': 'T2_log_bounce_power_law_mass',
        'description': (
            "S = c·L*(ε) = c(rs/2)ln(1/ε) ⟹ m = m_0 e^{−S} = m_0 ε^{c rs/2} = "
            "m_0 ε^p: the logarithm turns the exponential into a POWER LAW in "
            "the throat penetration depth ε (verified e^{−cL*} = ε^p)."
        ),
        'rows': rows,
        'relation': 'm ∝ ε^p, p = c·rs/2',
        'pass': ok,
    }


