# T3. The neutrino sector IS the log-bounce sector
# ---------------------------------------------------------------------------

def test_T3_neutrino_is_log_bounce() -> dict:
    """ν is the only genuine tunnelling sector (k=0, chargeless, #86/#88);
    m_ν ∝ ε^{4.8} (#112) is a power law. ε_n ∝ 1/χ_n (#79/#113) gives the right
    ordering, but the steep p=4.8 amplifies the χ_n spread: a ~×2 ε spread →
    2^4.8 ≈ 28× in mass — the ×28 overshoot of #113."""
    eps_n = [1.0 / c for c in CHI_N]
    eps_ratio = [round(e / eps_n[0], 2) for e in eps_n]
    # amplification of a representative ×2 ε spread
    amp_factor = 2.0 ** P_NU
    ordering_ok = eps_n[0] < eps_n[1] < eps_n[2]   # increasing ⟹ normal ordering
    return {
        'name': 'T3_neutrino_sector_is_log_bounce',
        'description': (
            "ν = the only tunnelling sector (k=0 chargeless, #86/#88); "
            "m_ν ∝ ε^{4.8} (#112), a power law. ε_n ∝ 1/χ_n (#79/#113) ⟹ right "
            "ordering (normal), but p=4.8 amplifies the χ_n spread: ×2 in ε → "
            "2^4.8 ≈ 28× in mass — the ×28 overshoot (#113). Form & ordering "
            "governed, value residual."
        ),
        'p_nu': P_NU,
        'chi_n': CHI_N,
        'eps_n_ratios': eps_ratio,
        'normal_ordering': ordering_ok,
        'amplification_2_to_p': round(amp_factor, 1),
        'overshoot_113': '×28 (= 2^4.8)',
        'pass': ordering_ok and abs(amp_factor - 27.86) < 1.0,
    }


