# T4. The quark Yukawas overflow the shell capacity
# ---------------------------------------------------------------------------

def test_T4_shell_overflow() -> dict:
    """The quark shell overtones (k=0, n=3,4,5) have ω = 3.82, 4.76, 5.70
    — a mass range of only ×1.49 — whereas the up-type quarks span
    m_t/m_u ≈ 8×10⁴. The geometric shell capacity is overflowed by ~×5×10⁴;
    n_part is the compensator for that overflow."""
    shell_span = _SHELL_OMEGA[2] / _SHELL_OMEGA[0]
    q = QUARK_MASS_MEV
    up_span = q['t'] / q['u']
    overflow = up_span / shell_span
    return {
        'name': 'T4_quark_yukawas_overflow_shell',
        'description': (
            "Shell ω(1,n=3,4,5) = 3.82, 4.76, 5.70 ⟹ mass range ×1.49; "
            "up-type quarks span ×8×10⁴. Shell capacity overflowed by "
            "~×5×10⁴; n_part compensates the overflow."
        ),
        'shell_omega_n345': _SHELL_OMEGA,
        'shell_mass_range': shell_span,
        'up_type_mass_span': up_span,
        'overflow_factor': overflow,
        'pass': shell_span < 2.0 and overflow > 1e4,
    }


