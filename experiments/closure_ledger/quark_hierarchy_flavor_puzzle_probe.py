# T2. RG-invariance of quark mass ratios
# ---------------------------------------------------------------------------

def test_T2_rg_invariance() -> dict:
    """QCD's mass anomalous dimension γ_m is flavor-universal, so under
    running every quark mass is multiplied by the SAME factor and the
    factor cancels in ratios: quark mass ratios are RG-invariant. Hence
    the hierarchy is NOT generated by α_s running (running sets the scale,
    not the ratios). Demonstrate the cancellation."""
    common_running_factor = 0.37          # same for all flavors (γ_m universal)
    q = QUARK_MASS_MEV
    ratio_before = q['c'] / q['u']
    ratio_after = (q['c'] * common_running_factor) / (q['u'] * common_running_factor)
    invariant = abs(ratio_after - ratio_before) / ratio_before < 1e-12
    return {
        'name': 'T2_quark_mass_ratios_rg_invariant',
        'description': (
            "γ_m flavor-universal ⟹ m_i(μ)/m_j(μ) is RG-invariant; the "
            "common running factor cancels. α_s running sets the overall "
            "scale, not the hierarchy ⟹ the hierarchy is NOT from running."
        ),
        'gamma_m_flavor_universal': True,
        'ratio_c_over_u_before': ratio_before,
        'ratio_c_over_u_after_running': ratio_after,
        'ratios_rg_invariant': invariant,
        'hierarchy_not_from_running': True,
        'pass': invariant,
    }


