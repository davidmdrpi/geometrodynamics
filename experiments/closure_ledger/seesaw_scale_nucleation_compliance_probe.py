def test_T4_barrier_height_hypothesis() -> dict:
    """Hypothesis A: M_R is the PR #58 nucleation barrier height
    E_c = (16π/3) σ³/ρ². Fix σ, ρ from the electron throat (geometric
    units, mc²=1, R*=1): brane tension B=4πσ with E(R*)=3BR*²=m_e c² ⟹
    σ R*² = m_e c²/(12π); bag density ρ R*³ = m_e c² over the throat
    volume (4/3)πR*³ ⟹ ρ R*³ = 3 m_e c²/(4π). Then E_c ≈ 0.0055 m_e c²
    ≈ 2.8 keV, R_c ≈ 0.22 R* — ~10⁸ below the required ~TeV. FALSIFIED."""
    # geometric units: mc² = 1, R* = 1
    mc2 = 1.0
    Rstar = 1.0
    sigma = mc2 / (12.0 * PI * Rstar ** 2)     # B=4πσ, BR*²=mc²/3
    rho = 3.0 * mc2 / (4.0 * PI * Rstar ** 3)   # rest energy / throat volume
    R_c = 2.0 * sigma / rho
    E_c_over_mc2 = (16.0 * PI / 3.0) * sigma ** 3 / rho ** 2
    E_c_keV = E_c_over_mc2 * M_E_MEV * 1e3
    E_c_eV = E_c_over_mc2 * M_E_MEV * 1e6

    rows = []
    for g in (1, 2, 3):
        M_R_req_eV = m_D_eV(g) ** 2 / NU_MASS_OBS_EV[g]
        rows.append({
            'generation': g,
            'M_R_required_eV': M_R_req_eV,
            'barrier_E_c_eV': E_c_eV,
            'ratio_M_R_over_E_c': M_R_req_eV / E_c_eV,
        })
    min_ratio = min(r['ratio_M_R_over_E_c'] for r in rows)
    barrier_matches_TeV = (E_c_keV * 1e-3 > 1e6)   # > 1 TeV? no.
    return {
        'name': 'T4_barrier_height_hypothesis_falsified',
        'description': (
            "Hypothesis A (M_R = nucleation barrier E_c=(16π/3)σ³/ρ²): "
            "with the electron-throat σ,ρ, E_c ≈ 2.8 keV, R_c ≈ 0.22 R* — "
            "~10⁸–10⁹ below the required ~TeV. The soft vacuum bubble is "
            "the maximally compliant channel; its barrier height is not "
            "the heavy Majorana scale. FALSIFIED."
        ),
        'sigma_geom': sigma,
        'rho_geom': rho,
        'R_c_over_R_star': R_c,
        'E_c_over_mc2': E_c_over_mc2,
        'E_c_keV': E_c_keV,
        'rows': rows,
        'min_ratio_M_R_over_E_c': min_ratio,
        'barrier_height_matches_TeV': barrier_matches_TeV,
        # the probe PASSES by correctly characterising the falsification:
        'pass': (not barrier_matches_TeV) and min_ratio > 1e6,
    }


