def test_T3_selection_rule_from_conservation() -> dict:
    """PR #58's conservation law Σc₁=0 applied to a SINGLE state flipping
    throat↔antithroat:
      - k=0 (c₁=0): flip gives 0 → −0 = 0; Σc₁ conserved with no partner
        ⟹ single-state ΔL=2 (Majorana) mass ALLOWED.
      - k≠0 (c₁=±1): single flip gives Σc₁ = ∓2 ≠ 0 ⟹ FORBIDDEN; a ΔL=2
        mass needs a PAIR, so no single-state Majorana ⟹ Dirac.
    This is exactly PR #86's only-neutrino result, derived from PR #58."""
    rows = []
    for label, k in [('neutrino', 0), ('charged lepton g1', 1),
                     ('charged lepton g2', 3), ('charged lepton g3', 5)]:
        c1 = 0 if k == 0 else 1
        # single state flips c₁ → −c₁; the change in total Hopf charge:
        delta_sigma_c1 = (-c1) - c1          # 0 for k=0, ∓2 for k≠0
        single_flip_conserves = (delta_sigma_c1 == 0)
        rows.append({
            'sector': label,
            'k': k,
            'c1': c1,
            'delta_sigma_c1_single_flip': delta_sigma_c1,
            'single_state_majorana_allowed': single_flip_conserves,
            'nature': 'Majorana' if single_flip_conserves else 'Dirac',
        })
    nu = rows[0]
    charged = rows[1:]
    rule_ok = (nu['single_state_majorana_allowed']
               and all(not r['single_state_majorana_allowed'] for r in charged))
    return {
        'name': 'T3_selection_rule_from_sigma_c1_zero',
        'description': (
            "PR #58's Σc₁=0 on a single flipping state: k=0 conserves "
            "(0→0) ⟹ Majorana allowed; k≠0 gives Σc₁=∓2 ⟹ forbidden ⟹ "
            "Dirac. PR #86's only-neutrino selection rule IS PR #58's "
            "conservation law for a single state."
        ),
        'rows': rows,
        'reproduces_pr86_only_neutrino': rule_ok,
        'pass': rule_ok,
    }


