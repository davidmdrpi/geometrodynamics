# T5. Winding θ-term ∮dφ = 2πn
# ---------------------------------------------------------------------------

def winding_number(phases: np.ndarray) -> float:
    """Total winding of a phase sequence around the circle (in units
    of 2π). phases is a closed path of angles; sum the wrapped
    increments and divide by 2π."""
    dphi = np.diff(phases)
    # wrap each increment to (−π, π]
    dphi_wrapped = (dphi + PI) % TAU - PI
    return float(np.sum(dphi_wrapped) / TAU)


def test_T5_winding_theta_term() -> dict:
    """The closure quantum is the winding number of the phase map
    S¹ → U(1): ∮dφ = 2π·n. A topological total-derivative term
    S_top = 2π·n. Metric-independent (depends only on winding, not on
    path length or the S³ radius)."""
    rows = []
    all_integer = True
    for n in [1, 2, 3, -1, 5]:
        # A phase path winding n times around the circle
        t = np.linspace(0.0, 1.0, 2001)
        phases = TAU * n * t
        w = winding_number(phases)
        S_top = TAU * w
        is_integer = abs(w - round(w)) < 1e-9 and abs(w - n) < 1e-9
        if not is_integer:
            all_integer = False
        rows.append({
            'intended_winding_n': n,
            'measured_winding': w,
            'S_top_2pi_n': S_top,
            'closure_quantum_match_2pi_n': abs(S_top - TAU * n) < 1e-9,
        })
    # Metric independence: rescale the path parameter; winding unchanged
    t = np.linspace(0.0, 1.0, 2001)
    phases_scaled = TAU * 1 * (t ** 3)   # nonuniform reparam, same endpoints
    w_scaled = winding_number(phases_scaled)
    metric_independent = abs(w_scaled - 1.0) < 1e-9
    return {
        'name': 'T5_winding_theta_term',
        'description': (
            "Closure quantum = winding number of the phase map "
            "S¹ → U(1): ∮dφ = 2π·n. Topological total-derivative term "
            "S_top = 2π·n. Integer-quantized by single-valuedness; "
            "metric-independent (reparam-invariant). This IS B1, now a "
            "term in the action rather than an imposed constraint."
        ),
        'rows': rows,
        'all_windings_integer': all_integer,
        'winding_metric_independent': metric_independent,
        'pass': all_integer and metric_independent,
    }


