def test_T2_double_cover() -> dict:
    """A great circle on S³ (geodesic length 2π) projects to a
    non-contractible RP³ loop (length π) traversed twice. The closure
    quantum (B1, 2π) is a double traversal of the RP³ generator (B2)."""
    rng = np.random.default_rng(1)
    rows = []
    max_diff = 0.0
    for _ in range(5):
        p = rng.normal(size=4)
        p = p / np.linalg.norm(p)
        ap = antipode4(p)
        rp3_loop_length = geo4(p, ap)       # half great circle = π
        great_circle_length = 2.0 * rp3_loop_length   # full = 2π
        diff = abs(rp3_loop_length - PI) + abs(great_circle_length - TAU)
        max_diff = max(max_diff, diff)
        if len(rows) < 4:
            rows.append({
                'rp3_loop_length_pi': rp3_loop_length,
                'great_circle_length_2pi': great_circle_length,
                'closure_quantum_2pi': TAU,
            })
    return {
        'name': 'T2_double_cover_closure_quantum',
        'description': (
            "Great circle on S³ (length 2π = closure quantum) = double "
            "traversal of the non-contractible RP³ loop (length π). The "
            "closure quantum (B1) and the antipodal Z₂ generator (B2) "
            "are the two faces of the S³ → RP³ double cover."
        ),
        'rows': rows,
        'max_difference': max_diff,
        'pass': max_diff < 1e-12,
    }


