# T6. Variational consistency of the winding term
# ---------------------------------------------------------------------------

def test_T6_variational_consistency() -> dict:
    """The winding θ-term S_top = 2π·n is a topological total
    derivative: it depends only on the homotopy class of the path, not
    on local field variations. Hence δS_top = 0 for any variation that
    preserves the winding (boundary-fixing), so it does NOT modify the
    local Euler–Lagrange equations. It can be added to the smooth
    action without conflict."""
    # Demonstrate: two paths in the same homotopy class (same winding,
    # same endpoints) have the same S_top regardless of their detailed
    # shape → the term is insensitive to local variation.
    t = np.linspace(0.0, 1.0, 4001)
    path_a = TAU * 1 * t                          # straight winding
    path_b = TAU * 1 * t + 0.3 * np.sin(2 * PI * t)  # wiggled, same endpoints/winding
    w_a = winding_number(path_a)
    w_b = winding_number(path_b)
    S_a = TAU * w_a
    S_b = TAU * w_b
    delta_S_top = abs(S_a - S_b)
    return {
        'name': 'T6_variational_consistency',
        'description': (
            "The winding θ-term is a topological total derivative: "
            "homotopic paths (same winding, same endpoints) give the "
            "same S_top. δS_top = 0 under local variations preserving "
            "winding, so the term does not modify the local EOM. The "
            "topological sector is variationally consistent with the "
            "smooth Einstein–Maxwell–Dirac–throat action."
        ),
        'S_top_straight_path': S_a,
        'S_top_wiggled_path': S_b,
        'delta_S_top_under_local_variation': delta_S_top,
        'pass': delta_S_top < 1e-9,
    }


