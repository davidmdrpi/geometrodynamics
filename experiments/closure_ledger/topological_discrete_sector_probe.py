# T3. π₁(RP³) = Z₂
# ---------------------------------------------------------------------------

def test_T3_pi1_rp3() -> dict:
    """π₁(RP³) = Z₂: a non-contractible loop (p → −p on S³, half great
    circle) squared lifts to a closed path on S³ (p → −p → p, full
    great circle). Since π₁(S³) = 0, the doubled loop is contractible,
    so [γ]² = 1 in π₁(RP³)."""
    rng = np.random.default_rng(2)
    rows = []
    closed_lift_ok = True
    for _ in range(4):
        p = rng.normal(size=4)
        p = p / np.linalg.norm(p)
        ap = antipode4(p)
        # Single RP³ loop: lift is the open S³ path p → −p (endpoints differ)
        single_open = float(np.linalg.norm(ap - p)) > 1e-9
        # Doubled RP³ loop: lift is p → −p → p (closed on S³)
        doubled_endpoint = antipode4(ap)   # = p
        doubled_closed = float(np.linalg.norm(doubled_endpoint - p)) < 1e-12
        if not (single_open and doubled_closed):
            closed_lift_ok = False
        rows.append({
            'single_loop_lift_is_open_path': single_open,
            'doubled_loop_lift_closes_on_S3': doubled_closed,
        })
    return {
        'name': 'T3_pi1_rp3_is_Z2',
        'description': (
            "π₁(RP³) = Z₂: the non-contractible RP³ loop lifts to an "
            "open S³ path (p → −p); its square lifts to a closed S³ "
            "path (p → −p → p), which is contractible since π₁(S³) = 0. "
            "Hence [γ]² = 1 in π₁(RP³) — the antipodal Z₂ is exactly the "
            "fundamental group of the quotient."
        ),
        'rows': rows,
        'pi1_is_Z2': closed_lift_ok,
        'pass': closed_lift_ok,
    }


