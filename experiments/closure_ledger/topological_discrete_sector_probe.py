# T1. Antipodal involution → Z₂
# ---------------------------------------------------------------------------

def test_T1_antipodal_involution() -> dict:
    """σ² = id (antipode4 is an involution). The quotient S³/Z₂ = RP³;
    the Z₂ is generated by σ."""
    samples = []
    max_resid = 0.0
    rng = np.random.default_rng(0)
    for _ in range(6):
        p = rng.normal(size=4)
        p = p / np.linalg.norm(p)
        ap = antipode4(p)
        aap = antipode4(ap)
        resid = float(np.linalg.norm(aap - p))
        # σ(p) = −p ≠ p (no fixed points → free action → smooth quotient)
        free_action = float(np.linalg.norm(ap - p))
        max_resid = max(max_resid, resid)
        if len(samples) < 4:
            samples.append({
                'p': p.tolist(),
                'sigma_p': ap.tolist(),
                'sigma_squared_p_minus_p': resid,
                'sigma_p_minus_p_should_be_nonzero': free_action,
            })
    return {
        'name': 'T1_antipodal_involution_Z2',
        'description': (
            "σ: p → −p is an involution (σ² = id) acting freely on S³ "
            "(no fixed points). The quotient S³/Z₂ = RP³ is a smooth "
            "manifold; the antipodal Z₂ (B2) becomes the deck "
            "transformation, a topological datum."
        ),
        'samples': samples,
        'max_involution_residual': max_resid,
        'pass': max_resid < 1e-14,
    }


